// Create a constant isAdult that receives a boolean value
// Display a message (with the person's name)
// indicating that they can buy if they are an adult

let name = "Rafael"
let age = 10
let isAdult: Bool = age >= 18

print("\(name) is an adult? \(isAdult)")

if isAdult {
    print("\(name) can buy.")
} else {
    print("\(name) cannot buy.")
}
