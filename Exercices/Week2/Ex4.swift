// Create a constant with a number between 1 and 7
// Display a message with the name of the day of the week in full
// Consider 1 as Sunday and use only if and else

let number = 6

if number == 1 {
    print("Sunday")
} else if number == 2 {
    print("Monday")
} else if number == 3 {
    print("Tuesday")
} else if number == 4 {
    print("Wednesday")
} else if number == 5 {
    print("Thursday")
} else if number == 6 {
    print("Friday")
} else if number == 7 {
    print("Saturday")
} else {
    print("Invalid number. Please enter a number between 1 and 7.")
}
